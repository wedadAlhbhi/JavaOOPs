package fun;

public class Arthimatic {

	
	public static void main(String[] args) {
		int m1=20;
		int m2=5;
		System.out.println("العمليات الأ,لية للعددين هي ");
		System.out.println(m1+m2);
		System.out.println(m1-m2);
		System.out.println(m1*m2);
		System.out.println(m1/m2);
		System.out.println(m1%m2);
		//using to shift bits of numbers left or right "AL Mutataliat"
		System.out.println("المتتاليلت الرقمية للعدد الثاني هي ");
		System.out.println(m2>>2);
		System.out.println(m2>>>4);
		System.out.println(m2<<3);
		System.out.println(m2<<5);

	}

}
